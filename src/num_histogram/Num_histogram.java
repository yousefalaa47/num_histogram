
package num_histogram;
import java .util.* ;
public class Num_histogram {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        String s = input.nextLine() ;
        int x = input.nextInt();
        for (int i = 1; i <= x; i++) {
            int y = input.nextInt();
            if (i != 1) {
                System.out.println();
            }
            for (int j = 1; j <= y; j++) {
                System.out.print(s);
                
            }
            
            
        }
        
    
    }
    
}
